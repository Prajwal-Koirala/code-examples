function main() {
    int()
    string()
    boolean()
    float()
}

main()

// int
function int() {
    var a = 1
    var b = 2
    console.log(a + b)
}

// String
function string() {
    var a = "hello"
    var b = "world"
    console.log(a + b)
}

// Boolean
function boolean() {
    var a = true
    console.log(a)
    var b = false
    console.log(b)
}

// Float
function float() {
    var a = 1.1
    var b = 2.2
    console.log(a + b)
}

function let() {
let a = "apple"
let b = "bees"
console.log(a, b)
}

function const() {
const a = "A"
console.log(a)
}
