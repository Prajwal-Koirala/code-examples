function main() {
    // Create a null value of a string.
    var nullString = null;
    console.log(nullString);
    nullString = "Apple";
    console.log(nullString);
    // Create a null value of a number.
    var nullNumber = null;
    console.log(nullNumber);
    nullNumber = 10;
    console.log(nullNumber);
    // Create a null value of a boolean.
    var nullBoolean = null;
    console.log(nullBoolean);
    nullBoolean = true;
    console.log(nullBoolean);
    // Create a null value of an array.
    var nullArray = null;
    console.log(nullArray);
    nullArray = [1, 2, 3];
    console.log(nullArray);
}

main();