def main():
    # String
    firstName = "John"
    print(firstName)
    # Integer
    age = 30
    print(age)
    # Float
    gpa = 3.9
    print(gpa)
    # Boolean
    isMale = True
    print(isMale)
    # List
    grades = [3.9, 3.5, 3.7]
    print(grades)
    # Tuple
    grades = (3.9, 3.5, 3.7)
    print(grades)
    # Dictionary
    grades = {"John": 3.9, "Jane": 3.5, "Jack": 3.7}
    print(grades)
    # Set
    grades = {3.9, 3.5, 3.7}
    print(grades)
    # None
    grades = None
    print(grades)

main()
