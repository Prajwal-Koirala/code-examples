package main

import (
	"fmt"
)


func main() {
	// operators
	fmt.Println(1 == 1) // Is equal
	fmt.Println(1 != 1) // Is not equal
	fmt.Println(1 < 1)  // Is less than
	fmt.Println(1 > 1)  // Is greater than
	fmt.Println(1 <= 1) // Is less than or equal to
	fmt.Println(1 >= 1) // Is greater than or equal to
}