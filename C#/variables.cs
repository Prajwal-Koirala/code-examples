using System;

namespace main
{
    class Program
    {
        static void Main(string[] args)
        {
            // This is a basic string.
            string first_name = "John";
            Console.WriteLine(first_name);
            // This is a basic integer.
            int age = 30;
            Console.WriteLine(age);
            // This is a basic float.
            float height = 1.8f;
            Console.WriteLine(height);
            // This is a basic boolean.
            bool is_married = false;
            Console.WriteLine(is_married);
            // This is a basic char.
            char initial = 'J';
            Console.WriteLine(initial);
            // This is a basic double.
            double gpa = 3.5;
            Console.WriteLine(gpa);
            // This is a basic long.
            long population = 7000000000;
            Console.WriteLine(population);
            // This is a basic short.
            short year = 2020;
            Console.WriteLine(year);
            // This is a basic byte.
            byte month = 12;
            Console.WriteLine(month);
            // This is a basic decimal.
            decimal money = 100.50m;
            Console.WriteLine(money);
        }
    }
}
