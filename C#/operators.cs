using System;

namespace main
{
    class Program
    {
        static void Main(string[] args)
        {
            // A simple example of adding two numbers together using +
            int a = 10;
            int b = 20;
            int c = a + b;
            Console.WriteLine(c);
            // A simple example of subtracting two numbers together using -
            int d = 10;
            int e = 20;
            int f = e - d;
            Console.WriteLine(f);
            // A simple example of multiplying two numbers together using *
            int g = 10;
            int h = 20;
            int i = g * h;
            Console.WriteLine(i);
            // A simple example of dividing two numbers together using /
            int j = 10;
            int k = 20;
            int l = k / j;
            Console.WriteLine(l);
            // A simple example of modulus two numbers together using %
            int m = 10;
            int n = 20;
            int o = n % m;
            Console.WriteLine(o);
        }
    }
}
