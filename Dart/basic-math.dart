void main() {
  // We will add two numbers.
  int a = 1;
  int b = 2;
  // The result will be stored in `c`.
  int c = a + b;
  // Prints `3`.
  print(c);

  // We can also use the `+` operator.
  print(5 + 5);

  // We can also use the `-` operator.
  print(5 - 39);

  // We can also use the `*` operator.
  print(5 * 343);

  // We can also use the `/` operator.
  print(5 / 3);

  // We can also use the `%` operator.
  print(5 % 3);

  // We can also use the `<<` operator.
  print(5 << 2);

  // We can also use the `>>` operator.
  print(5 >> 2);

  // We can also use the `&` operator.
  print(5 & 3);

  // We can also use the `|` operator.
  print(5 | 3);

  // We can also use the `^` operator.
  print(5 ^ 3);
}
