import "dart:core";

void main() {
  // Check if the first value is greater than or equal to the second value.
  print(primaryValueGreaterOrEqualToSecondary(10, 5));
}

// Check if the first value is greater than or equal to the second value.
bool primaryValueGreaterOrEqualToSecondary(
  final int primary,
  final int secondary,
) {
  return primary >= secondary;
}
