void main() {
  // Get the length of a string
  var name = "John Doe";
  print(name.length);

  // Get the length of a list
  var list = [1, 2, 3, 4, 5];
  print(list.length);

  // Get the length of a map
  var map = {"name": "John Doe", "age": 30, "isMarried": false};
  print(map.length);

  // Get the length of a set
  var set = {1, 2, 3, 4, 5};
  print(set.length);
}