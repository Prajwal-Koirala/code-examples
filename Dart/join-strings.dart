void main() {
  // Primary string
  String hello = "Hello";
  // Secondary string
  String world = "World";
  // Add two strings together
  print("${hello} ${world}");
  // Add two strings together without using ${} ${}
  print(hello + world);
  // More examples of adding strings.
  print(hello + " " + world);
  print(hello + " " + world + "!");
  print(hello + " " + world + "!" + " " + "add more stuff here.");
}
