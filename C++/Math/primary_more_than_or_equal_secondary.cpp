#include <iostream>

// Check if the primary value is more than or equal to the secondary value.
bool primaryMoreThanOrEqualToSecondary(int primary, int secondary) {
    return primary >= secondary;
}

int main() {
    // Check if the primary value is more than or equal to the secondary value.
    printf("%d \n", primaryMoreThanOrEqualToSecondary(25, 10));
    return 0;
}